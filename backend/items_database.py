def search_items(search_term):
    """Search items by ID or name"""
    search_term = search_term.lower()
    results = {}
    for category, items in ALBION_ITEMS.items():
        for item_id, item_name in items.items():
            if search_term in item_id.lower() or search_term in item_name.lower():
                results[item_id] = item_name
    return results